# Verificar si un numero es postivo, negativo o cero

import os; os.system("clear")
print("Verificando si un numero entero es positivo negativo o cero")

numero = int(input("Dame un numero ? "))

if numero>0 :
    print("El numero es positivo")
    
if numero<0 :
    print("El numero es negativo")

if numero==0 :
    print("El numero es cero")

